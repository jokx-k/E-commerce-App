package com.example.ecommerce;

public class Customer {
    public String Name , Username , Password , BirthDate , Email , Question , Answer;
    public Customer(String name, String username, String password, String birthDate, String email , String question , String answer) {
        Name = name;
        Username = username;
        Password = password;
        BirthDate = birthDate;
        Email = email;
        Question = question;
        Answer = answer;
    }
}
